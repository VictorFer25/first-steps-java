    public void verficaciones() {

        //limitar que solo sean caracteres del alfabeto
        this.val.soloLetras(this.jtNombre);
        this.val.soloLetras(this.jtPrimerApellido);
        this.val.soloLetras(this.jtSegundoApellido);
        this.val.soloNumeros(this.jtTelefono);

        //limitar los caracteres
        this.val.limitarNumeros(this.jtTelefono, 10);
        this.val.limitarNumeros(this.jtNombre, 20);
        this.val.limitarNumeros(this.jtPrimerApellido, 20);
        this.val.limitarNumeros(this.jtSegundoApellido, 20);
        this.val.limitarNumeros(this.jtUsuario, 20);
        this.val.limitarNumeros(this.jtCruzamiento1, 20);
        this.val.limitarNumeros(this.jtCruzamiento2, 20);
        this.val.limitarNumeros(this.jtCodigoPostal, 4);
    }

