    public void cargarDatosPersonal(ResultSet datos) {
        try {
            while (datos.next()) {
                this.personal_id = datos.getString("personal_id");
                this.jtNombre.setText(datos.getString("personal_nombre"));
                this.jtPrimerApellido.setText(datos.getString("personal_primer_apellido"));
                this.jtSegundoApellido.setText(datos.getString("personal_segundo_apellido"));
                this.jtCalle.setText(datos.getString("personal_calle"));
                this.jtTelefono.setText(datos.getString("personal_telefono"));
                this.jtColonia.setText(datos.getString("personal_colonia"));
                this.jtUsuario.setText(datos.getString("personal_usuario"));
                this.jtPassword.setText(datos.getString("personal_password"));
                this.jComboBox1.setSelectedItem(datos.getString("personal_tipopersonal"));
                this.jtCruzamiento1.setText(datos.getString("cruzamiento_1"));
                this.jtCruzamiento2.setText(datos.getString("cruzamiento_2"));
                this.jtCodigoPostal.setText(String.valueOf(datos.getInt("codigo_postal")));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

