    private void jLabel10MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MousePressed
        // TODO add your handling code here:

        String password = "";

        JTextField[] datos = new JTextField[3];
        Object[] datosAct = new Object[13];
        datos[0] = this.jtNombre;
        datos[1] = this.jtPrimerApellido;
        datos[2] = this.jtUsuario;

        boolean vacio = true;

        for (int i = 0; i < datos.length; i++) {
            if (datos[i].getText().length() == 0) {
                vacio = false;
                datos[i].setBackground(Color.red);
            }
        }

        if (vacio) {

            if (this.jtPrimerApellido.isVisible()) {
                password = this.jtPassword.getText();
            } else {
                if (this.jTextField9.isVisible()) {
                    password = this.jTextField9.getText();
                }
            }

            datosAct[0] = this.personal_id;
            datosAct[1] = this.jtNombre.getText();
            datosAct[2] = this.jtPrimerApellido.getText();
            datosAct[3] = this.jtSegundoApellido.getText();
            datosAct[4] = this.jtCalle.getText();
            datosAct[5] = this.jtTelefono.getText();
            datosAct[6] = this.jtColonia.getText();
            datosAct[7] = this.jtUsuario.getText();
            datosAct[8] = password;
            datosAct[9] = this.jComboBox1.getSelectedItem();
            datosAct[10] = this.jtCruzamiento1.getText();
            datosAct[11] = this.jtCruzamiento1.getText();
            datosAct[12] = this.jtCodigoPostal.getText();

            this.eP.setGestionarPersonal(this.personal);
            this.eP.setIep(this);
            this.eP.actualizarPersonal(datosAct);
        }
    }//GEN-LAST:event_jLabel10MousePressed

