    public void cargarDatosPersonal(ResultSet rs) {
        try {
            while (rs.next()) {
                this.jcbTipoUsuario.setSelectedItem(rs.getString("usuario_tipo"));
                this.jtMatricula.setText(rs.getString("usuario_id"));
                this.jtNombre.setText(rs.getString("usuario_nombre"));
                this.jtApaterno.setText(rs.getString("usuario_primer_apellido"));
                this.jtAmaterno.setText(rs.getString("usuario_segundo_apellido"));
                this.jtFehaNac.setDate(rs.getDate("usuario_fecha"));
                this.jtCalle.setText(rs.getString("usuario_calle"));
                this.jtCorreo.setText(rs.getString("usuario_correo"));
                this.jtTelefono.setText(rs.getString("usuario_telefono"));
                this.jtColonia.setText(rs.getString("usuario_colonia"));
                this.jtCalle.setText(rs.getString("usuario_calle"));
                this.jtCruzamiento1.setText(rs.getString("usuario_cruzamiento1"));
                this.jtCruzamiento2.setText(rs.getString("usuario_cruzamiento2"));
                this.jtNumCasa.setText(rs.getString("usuario_numcasa"));
                this.jtLugarTrabajo.setText(rs.getString("usuario_trabajo"));
                this.jtPuestoTrabajo.setText(rs.getString("usuario_puesto"));
            }
        } catch (Exception e) {
            System.out.println("falló");
            e.printStackTrace();
        }
    }

 


