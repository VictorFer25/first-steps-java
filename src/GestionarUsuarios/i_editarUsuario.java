    public void validaciones() {
        validacionUsuario v = new validacionUsuario();
        //validando matricula
        v.validarNumeros(jtMatricula);
        v.limitarCaracteres(jtMatricula, 8);
        //validando nombre
        v.validarLetras(jtNombre);
        v.limitarCaracteres(jtNombre, 20);
        //validando primer apellido
        v.validarLetras(jtApaterno);
        v.limitarCaracteres(jtApaterno, 20);
        //validando segundo apellido
        v.validarLetras(jtAmaterno);
        v.limitarCaracteres(jtAmaterno, 20);
        //validando fecha
        //validando telefono
        v.validarNumeros(jtTelefono);
        v.limitarCaracteres(jtTelefono, 10);
        //validar colonia
        v.limitarCaracteres(jtColonia, 30);
        //validar calle
        v.limitarCaracteres(jtCalle, 30);
        //validar cruzamientos uno
        v.limitarCaracteres(jtCruzamiento1, 30);
        //validar cruzamiento dos
        v.limitarCaracteres(jtCruzamiento2, 30);
        //validar numero de casa
        v.limitarCaracteres(jtNumCasa, 12);
        // validar lugar de trabajo
        v.validarLetras(jtLugarTrabajo);
        v.limitarCaracteres(jtLugarTrabajo, 20);
        //validar puesto de trabajo
        v.validarLetras(jtPuestoTrabajo);
        v.limitarCaracteres(jtPuestoTrabajo, 10);
    }
