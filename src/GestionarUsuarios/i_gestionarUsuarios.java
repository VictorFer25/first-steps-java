    //Este metodo carga datos en la tabla
    public void mostrarListaUsuarios(ResultSet listaUsuarios) {
        limpiarTabla();
        try {

            while (listaUsuarios.next()) {
                Object row[] = new Object[4];
                for (int i = 0; i < 4; i++) {
                    row[i] = listaUsuarios.getObject(i + 1); //El resulset los indices empiezan en 1, mi for lo empieso en 0 por eso le sumo i+1
                }
                //Agregamos cada registro al modelo de la tabla
                modelo.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los datos de los usuarios.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    // Este metodo limpia la tabla para ingresar nuevos datos
    private void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }

