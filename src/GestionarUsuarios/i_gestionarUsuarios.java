    public void validaciones() {
        validacionUsuario v = new validacionUsuario();
        if (this.jcbFiltros.getSelectedItem().equals("Matricula")) {
            v.validarNumeros(jtBuscar);
            v.limitarCaracteres(jtBuscar, 10);
        }
        if (this.jcbFiltros.getSelectedItem().equals("Nombre")) {
            v.validarLetras(jtBuscar);
            v.limitarCaracteres(jtBuscar, 10);
        }
    }

