    //Este metodo se ejecuta cuando hacemos click sobre el boton editar
    private void btn_editarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_editarMousePressed
        //obtiene el fila seleccionada
        int seleccion = this.jTUsuarios.getSelectedRow();
        //si hay una fila seleccionada se ejecuta el siguiete codigo
        if (seleccion != -1) {
            //enviamos el objeto a nuestro control
            c_editarUsuario obj_cEditarUsuario = new c_editarUsuario();
            obj_cEditarUsuario.setEditarPersonal(this);
            //Obtenemos la matricula 
            String matricula = String.valueOf(jTUsuarios.getValueAt(seleccion, 0));
            //preparamos la interfaz y la mostramos
            obj_cEditarUsuario.obtenerDatosUsuario(matricula);
            obj_cEditarUsuario.prepararInterfazEditarUsuario();
        } else {
            //se manda un mensaje a pantalla para que se seleccione una fila
            JOptionPane.showMessageDialog(null, "Seleccione una Fila", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_editarMousePressed

    //Metodo que se ejecuta cuando se presiona el boton eliminar
    private void btn_eliminarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_eliminarMousePressed
        int selec = this.jTUsuarios.getSelectedRow();
        if (selec != -1) {
            this.jTUsuarios.clearSelection();
            int resp = JOptionPane.showConfirmDialog(null, "¿Eliminar Usuario"+ this.jTUsuarios.getValueAt(selec, 1) +"?", "Alerta!", JOptionPane.YES_NO_OPTION);
            //Si acepta el mesaje procedemos a eliminar
            if (resp == JOptionPane.YES_OPTION) {
                c_gestionarUsuario cUsuario = new c_gestionarUsuario();
                String personal_Id = String.valueOf(this.jTUsuarios.getValueAt(selec, 0));
                //llamamos al metodo eliminar
                boolean confir = cUsuario.eliminarRegistroUsuario(personal_Id);
                if (confir) {
                    this.mostrarListaUsuarios(cUsuario.getListaPersonal());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Elija una fila", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_eliminarMousePressed

