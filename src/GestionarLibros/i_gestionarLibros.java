    //Metodo que carga en la tabla todos los libros
    public void mostrarListaLibros(ResultSet listalibros) {
        limpiarTablaLibros();
        try {

            while (listalibros.next()) {
                Object row[] = new Object[7];
                for (int i = 0; i < 7; i++) {
                    row[i] = listalibros.getObject(i + 1); //El resulset los indices empiezan en 1, mi for lo empieso en 0 por eso le sumo i+1
                }
                //Agregamos cada registro al modelo de la tabla
                modelo.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los datos de los libros.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

//Metodo que limpia la tabla de libros
    private void limpiarTablaLibros() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }

