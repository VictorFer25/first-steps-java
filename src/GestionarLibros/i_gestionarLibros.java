    private void jTBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTBuscarKeyReleased
        String busq = this.jTBuscarLibros.getText();

        //creo un objeto de tipo patron para verifcar el lenguaje que 
        //voy a usar en lo sera el modulo de buscar por id
        Pattern patron = Pattern.compile("[0-9]*");

        //compruebo que busq pertenezca al lenguaje
        Matcher m = patron.matcher(busq);

        //esto retorna un booleano para ver si busq se encuentra dentro
        //del lenguaje
        boolean b = m.matches();

        if (jTBuscar.getText().length() != 0) {
            if (jCFiltros.getSelectedItem().equals("ID")) {
                if (b) {

                    this.cgl.buscarIDAutor(busq);
                } else {
                    this.cgl.recargarListaAutores();
                }

            }

            if (jCFiltros.getSelectedItem().equals("Nombre")) {
                cgl.buscarNombreAutor(jTBuscar.getText());
            }
            if (jCFiltros.getSelectedItem().equals("Primer apellido")) {
                cgl.buscarPrimerApellidoAutor(jTBuscar.getText());
            }
        } else {
            cgl.recargarListaAutores();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jTBuscarKeyReleased

